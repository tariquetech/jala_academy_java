class SmallAndLargeNo {
	
	public static void main(String[] args){
         
         int a = 100;
         int b = 600;
       
         if(a>b){
            System.out.println("Large No is " + a);
            System.out.println("Small No is " + b);
         }else if(a==b){
            System.out.println("Both No is equal");
         }else{
            System.out.println("Large No is " + b);
            System.out.println("Small No is " + a);
         }

	}

}